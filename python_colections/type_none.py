"""

Tipo None

O tipo de dado None em Python representa sem tipo, ou poderia ser conhecido tambem como tipo vazio,
porem, falar que e um tipo sem tipo e mais apropriado.

OBS: O tipo None e sempre espeficidado com a primeira letra maiuscula.

Quando utilizamos?

- Podemos utilizar None quando queremos criar uma variavel e inicializa-la com um tipo sem tipo, antes
de recebermos um valor final.

"""
numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.23, 1.45

print(numeros)
print(type(numeros))

